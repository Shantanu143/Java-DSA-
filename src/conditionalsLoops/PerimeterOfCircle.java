package conditionalsLoops;

//Perimeter Of Circle
public class PerimeterOfCircle {


    public static void main(String[] args) {

        int r = 5;

//        formula  2  * pie *  r

        final float pie = 3.14f;

        float perimeterOfCircle = 2 * pie * r;

        System.out.println(perimeterOfCircle);


    }

}
