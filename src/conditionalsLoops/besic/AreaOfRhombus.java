package conditionalsLoops.besic;

//Area Of Rhombus
public class AreaOfRhombus {

    public static void main(String[] args) {

        float b = 8;
        float h = 64;

        //  Area Of Rhombus 1/2 * base * height (particular distance between two opposite side )


        double areaOfRhombus = 0.5 * b * h;
        System.out.println(areaOfRhombus);

    }

}
